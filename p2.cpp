#include<iostream>
using namespace std;

int main() {
    float num1, num2;

    cout << "Enter the first numbers: " << endl;
    cin >> num1;

    cout << "Enter the second numbers: " << endl;
    cin >> num2;

    cout << "Addition of " << num1 << " and " << num2 << " is " << num1 + num2 << endl;
    cout << "Subtraction of " << num1 << " and " << num2 << " is " << num1 - num2 << endl;
    cout << "Multiplication of float numbers is " << num1 * num2 << endl;
    cout << "Division of float numbers is " << num1 / num2 << endl;
    return 0;
}